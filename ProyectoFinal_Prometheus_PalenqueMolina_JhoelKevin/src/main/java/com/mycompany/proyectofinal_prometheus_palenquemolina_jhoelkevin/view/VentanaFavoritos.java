    private void btnVerDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerDetallesActionPerformed
        int fila = tblFavoritos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un libro favorito", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String titulo = (String) tablaModel.getValueAt(fila, 1);
        int idLibro = libroController.obtenerIdPorTitulo(titulo);
        if (idLibro != -1) {
            Libro libro = libroController.obtenerPorId(idLibro);
            VentanaLibroDetalles detallesWin = new VentanaLibroDetalles(libro, usuarioActivo);
            detallesWin.setVisible(true);
        }
    }//GEN-LAST:event_btnVerDetallesActionPerformed

    private void btnQuitarFavoritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarFavoritoActionPerformed
        int fila = tblFavoritos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un libro favorito para quitar", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String titulo = (String) tablaModel.getValueAt(fila, 1);
        int idLibro = libroController.obtenerIdPorTitulo(titulo);
        if (idLibro != -1) {
            boolean ok = favoritoController.eliminarFavorito(usuarioActivo.getId(), idLibro);
            if (ok) {
                JOptionPane.showMessageDialog(this, "Libro quitado de favoritos");
                cargarTablaFavoritos();
            }
        }
    }//GEN-LAST:event_btnQuitarFavoritoActionPerformed

