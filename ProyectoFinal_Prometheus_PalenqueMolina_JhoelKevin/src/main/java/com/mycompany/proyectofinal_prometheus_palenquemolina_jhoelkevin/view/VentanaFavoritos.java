    private void cargarTablaFavoritos() {
        tablaModel.setRowCount(0);
        List<Libro> lista = favoritoController.obtenerFavoritos(usuarioActivo.getId());
        for (Libro l : lista) {
            ImageIcon icono = crearIconoPortada(l.getPortadaURL());
            tablaModel.addRow(new Object[]{ icono, l.getTitulo(), l.getAutor() });
        }
    }
    
    
    /**
     * Dado el string ruta , devuelve un ImageIcon escalado.
     */
    private ImageIcon crearIconoPortada(String ruta) {
        try {
            URL url;
            if (ruta.startsWith("http://") || ruta.startsWith("https://") || ruta.startsWith("file:")) {
                url = new URL(ruta);
            } else {
                // Intentamos cargar como recurso en classpath /images/
                URL r = getClass().getResource("/images/" + ruta);
                if (r != null) {
                    url = r;
                } else {
                    // Finalmente lo tratamos como fichero local
                    url = new File(ruta).toURI().toURL();
                }
            }
            Image img = ImageIO.read(url).getScaledInstance(80, 120, Image.SCALE_SMOOTH);
            return new ImageIcon(img);
        } catch (IOException e) {
            // Fallback: icono por defecto si existe
            URL def = getClass().getResource("/images/defaultCover.png");
            if (def != null) {
                try {
                    Image img = ImageIO.read(def).getScaledInstance(80, 120, Image.SCALE_SMOOTH);
                    return new ImageIcon(img);
                } catch (IOException ex) { }
            }
            return null;
        }
    }
